<template lang="html">
    <div class="achievements-index">
        <myTable ref="listTable" :listConf="listConf" @select="selectRow" @searchReset="searchReset">
            <template slot="formItem" >
                <Form-item prop="keyword">
                    <Input type="text" v-model="listConf.searchParams.keyword" placeholder="搜索销售姓名/手机号" ></Input>
                </Form-item>
            </template>

            <template slot="formBtn" slot-scope="params">
                <Button type="primary" @click="jumpPage({ path: '/dataManage/achievements/add' })"><Icon type="plus-round"></Icon>业绩录入</Button>
                <ButtonGroup>
                    <Button type="primary" @click="exportData(0)"><Icon type="md-cloud-download" /> 导出当前页</Button>
                    <Button type="primary" :loading="params.exportLoading" @click="exportData(1)"> 全部</Button>
                </ButtonGroup>
            </template>
        </myTable>
    </div>
</template>

<script>
import Util from '@/libs/util'
import myTable from '@/view/includes/myTable'

export default {
    components: {
        myTable
    },
    data() {
        return {
            currentRow: {},
            institutions: [],
            listConf: {
                url: '/adminapi/achievements',
                searchParams: {
                    keywords: ''
                },
                item: [],
                columns: [
                    { type: 'index', align: 'center', width: 100, fixed: 'left' },
                    { title: '所属销售', align: 'center', key: 'sellername', width: 150, render: (h, params) => {
                        return params.row.seller ? h('span', params.row.seller.name + '-' + params.row.seller.phone) : h('span', '')
                    }},
                    { title: '所在地区', align: 'center', key: 'region_name', width: 200, render: (h, params) => {
                        var region_name = ''
                        var first = true
                        for (let region of params.row.region_name_arr) {
                            if (first) {
                                region_name += region
                            } else {
                                region_name += '-' + region
                            }
                            first = false
                        }
                        return h('span', region_name)
                    }},
                    { title: '相关客户', align: 'center', key: 'customername', width: 150, render: (h, params) => {
                        return params.row.customer ? h('span', params.row.customer.name + '-' + params.row.customer.phone) : h('span', '')
                    }},
                    { title: '销售产品', align: 'center', key: 'productname', width: 200, render: (h, params) => {
                        return params.row.product ? h('span', params.row.product.name) : h('span', '')
                    }},
                    { title: '销售单价', align: 'center', key: 'price', width: 100 },
                    { title: '销售数量', align: 'center', key: 'sale_num', width: 100 },
                    { title: '优惠金额', align: 'center', key: 'discount_money', width: 100 },
                    { title: '应销售总额', align: 'center', key: 'should_money', width: 100 },
                    { title: '实际销售总额', align: 'center', key: 'real_money', width: 120 },
                    { title: '备注', align: 'center', key: 'remark', width: 200 },
                    { title: '添加时间', align: 'center', key: 'created_at', width: 150},
                    { title: '操作', key: 'action', align: 'center', width: 150, fixed: 'right', render: (h, params) => {
                        return h('div', [
                            h('Button', {
                                props: {
                                    type: 'primary',
                                    size: 'small',
                                    icon: 'md-brush'
                                },
                                style: {
                                    marginRight: '5px',
                                    marginBottom: '5px'
                                },
                                on: {
                                    click: () => {
                                        var id = params.row.id
                                        this.jumpPage('/dataManage/achievements/edit/' + id)
                                    }
                                }
                            }),
                            h('Button', {
                                props: {
                                    type: 'error',
                                    size: 'small',
                                    icon: 'md-close'
                                },
                                style: {
                                    marginRight: '5px',
                                    marginBottom: '5px'
                                },
                                on: {
                                    click: () => {
                                        var id = params.row.id
                                        this.deleteConf(id)
                                    }
                                }
                            })
                        ])
                    }}
                ]
            },
            deleteId: 0
        }
    },
    methods: {
        selectRow: function(index) {
            this.currentRow = index
        },
        searchReset: function(originParams) {
            this.listConf.searchParams = Util.extend(this.listConf.searchParams, originParams)
        },
        jumpPage: function(path) {
            this.$router.push(path)
        },
        deleteConf: function(id) {
            var _this = this
            _this.$Modal.confirm({
                title: '提示',
                content: '确定删除吗？删除之后不可恢复!',
                onOk: function() {
                    _this.achievementDel(id)
                },
                onCancel: function() {
                    _this.$Notice.error({
                        title: '提示',
                        desc: '操作取消'
                    })
                }
            })
        },
        achievementDel(id) {
            var _this = this
            Util.ajax({
                url: '/adminapi/achievements/' + id,
                method: 'DELETE',
                success: function(result) {
                    if (result.error == 0) {
                        _this.$Notice.success({
                            title: '提示',
                            desc: '删除成功'
                        })
                        _this.$refs.listTable.listLoad()
                    } else {
                        _this.$Notice.error({
                            title: '提示',
                            desc: result.info
                        })
                    }
                }
            })
        },
        exportData: function (isAll) {
            this.$refs.listTable.exportCsv({
                isAll: isAll,
                columnsCB: function (item, index, columns) {
                    return index > 0 && index <= 11;
                },
                dataCB: function (item, index, data) {
                    item.sellername = item.seller ? item.seller.name + '-' + item.seller.phone : '';

                    var region_name = ''
                    var first = true
                    for (let region of item.region_name_arr) {
                        if (first) {
                            region_name += region
                        } else {
                            region_name += '-' + region
                        }
                        first = false
                    }

                    item.region_name = region_name;
                    item.customername = item.customer ? item.customer.name + '-' + item.customer.phone : '';
                    item.productname = item.product ? item.product.name : '';

                    return item;
                },
            });
        }
    },
    created: function() {
        var _this = this
    },
    mounted: function() {}
}
</script>

